class lexema():
    def __init__(self, lexe, fila, columna):
        self.lexe = lexe
        self.fila = fila
        self.columna = columna
    
    def verLexe(self):
        return self.lexe
    
    def verFila(self):
        return self.fila

    def verColumna(self):
        return self.columna
